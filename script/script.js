const massage = 'board updated successfully';
document.getElementById('completed').addEventListener('click', 
    function (event) {
        event.preventDefault();
        
            document.getElementById('task-1').style.display='block';
            alert(massage);
            const click = document.getElementById('completed');
            const point = document.getElementById('num').innerText;
            const convertedPoint = parseInt(point);
            const add = document.getElementById('add').innerText;
            const convertedAdd = parseInt(add);
            
                const sum = convertedPoint - 1;
                document.getElementById('num').innerText= sum;
                const Sum = convertedAdd + 1;

                document.getElementById('add').innerText= Sum;
                click.disabled= true;
            
   
})
